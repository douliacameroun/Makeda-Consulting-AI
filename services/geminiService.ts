
import { GoogleGenAI, GenerateContentResponse, Chat, Modality } from "@google/genai";
import { SYSTEM_PROMPT } from "../constants";

export class GeminiService {
  private ai: GoogleGenAI;
  private chat: Chat | null = null;
  private currentAudioContext: AudioContext | null = null;
  private currentSource: AudioBufferSourceNode | null = null;

  constructor() {
    this.ai = new GoogleGenAI({ apiKey: process.env.API_KEY || '' });
  }

  private initChat() {
    this.chat = this.ai.chats.create({
      model: 'gemini-3-pro-preview',
      config: {
        systemInstruction: SYSTEM_PROMPT,
        temperature: 0.6,
      },
    });
  }

  async sendMessage(message: string): Promise<string> {
    if (!this.chat) {
      this.initChat();
    }
    
    try {
      const result = await this.chat!.sendMessage({ message });
      // On nettoie les balises HTML si elles apparaissent par erreur, mais on garde les astérisques pour le Markdown
      return (result.text || "").replace(/<[^>]*>?/gm, '').trim();
    } catch (error) {
      console.error("Gemini Error:", error);
      return "Désolé, une erreur technique est survenue. Nos experts Makeda restent à votre disposition.";
    }
  }

  async generateSpeech(text: string): Promise<string | undefined> {
    try {
      // Nettoyage du texte pour la synthèse vocale (on retire les symboles Markdown pour la voix)
      const speechText = text.replace(/\*\*/g, '').substring(0, 1000);
      
      const response = await this.ai.models.generateContent({
        model: "gemini-2.5-flash-preview-tts",
        contents: [{ parts: [{ text: speechText }] }],
        config: {
          responseModalities: [Modality.AUDIO],
          speechConfig: {
            voiceConfig: {
              prebuiltVoiceConfig: { voiceName: 'Kore' }, // Voix féminine d'élite
            },
          },
        },
      });

      return response.candidates?.[0]?.content?.parts?.[0]?.inlineData?.data;
    } catch (error) {
      console.error("TTS Error:", error);
      return undefined;
    }
  }

  stopAudio() {
    if (this.currentSource) {
      try {
        this.currentSource.stop();
      } catch (e) {}
      this.currentSource = null;
    }
    if (this.currentAudioContext) {
      this.currentAudioContext.close().catch(() => {});
      this.currentAudioContext = null;
    }
  }

  async playBase64Audio(base64: string) {
    this.stopAudio();
    
    try {
      this.currentAudioContext = new (window.AudioContext || (window as any).webkitAudioContext)({ sampleRate: 24000 });
      const audioBuffer = await this.decodeAudioData(this.decode(base64), this.currentAudioContext, 24000, 1);
      
      this.currentSource = this.currentAudioContext.createBufferSource();
      this.currentSource.buffer = audioBuffer;
      this.currentSource.connect(this.currentAudioContext.destination);
      this.currentSource.start(0);
      
      this.currentSource.onended = () => {
        this.currentSource = null;
      };
    } catch (err) {
      console.error("Audio Playback Error:", err);
    }
  }

  private decode(base64: string) {
    const binaryString = atob(base64);
    const len = binaryString.length;
    const bytes = new Uint8Array(len);
    for (let i = 0; i < len; i++) {
      bytes[i] = binaryString.charCodeAt(i);
    }
    return bytes;
  }

  private async decodeAudioData(data: Uint8Array, ctx: AudioContext, sampleRate: number, numChannels: number): Promise<AudioBuffer> {
    const dataInt16 = new Int16Array(data.buffer);
    const frameCount = dataInt16.length / numChannels;
    const buffer = ctx.createBuffer(numChannels, frameCount, sampleRate);
    for (let channel = 0; channel < numChannels; channel++) {
      const channelData = buffer.getChannelData(channel);
      for (let i = 0; i < frameCount; i++) {
        channelData[i] = dataInt16[i * numChannels + channel] / 32768.0;
      }
    }
    return buffer;
  }
}

export const geminiService = new GeminiService();
